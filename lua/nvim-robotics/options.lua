-- *** TAB ***

-- # Enable auto indentation for next line (copied from current line)
vim.opt.autoindent = true
-- # Auto indentation uses 4 spaces by default
vim.opt.shiftwidth = 4
-- # Convert a tab into spaces
vim.opt.expandtab = true
-- # Generate 4 spaces per tab or delete 4 spaces at once with backspace
vim.opt.softtabstop = 4 
